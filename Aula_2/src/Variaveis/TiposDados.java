package Variaveis;

public class TiposDados {

	public static void main(String[] args) {
		// byte = -128 a 127
		byte numero = 127;
		
		// short = -32.767 a 32.767
		short n2 = 300;
		
		// int = -2.147.483.648 a 2.147.483.648
		int idade = 40;
		
		//long
		long n3 = 500;
		
		// Tipos Decimais
		// float = 3,4 * 10^38 a 3,4 * 10^38
		float peso = 30.60f ;
		//double -1,8 * 10 ^ 308 a 1,8 * 10 ^ 308
		
		//Tipos Texto
		// "" - String
		// '' - Char
		char genero = 'F';
		String nome = "Pedro" ;
		
		//Tipo boleano
		boolean logico = true;
		

	}

}
